    /////////////////////////////////////////////////// INIT ///////////////////////////////////////////////////////
    // Use this for initialization


    /////////////////////////////////////////////////// EVENTS /////////////////////////////////////////////////////

    /////////////////////////////////////////////////// HELPERS ////////////////////////////////////////////////////
    //ajouter le pouvoir
    public override void AddPower() {
